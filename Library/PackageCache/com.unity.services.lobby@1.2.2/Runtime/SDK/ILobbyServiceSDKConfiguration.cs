using System.Threading.Tasks;
using Unity.Services.Lobbies.Models;

namespace Unity.Services.Lobbies
{
    /// <summary>
    /// Interface used for editing the configuration of the lobby service SDK.
    /// Primary usage is for testing purposes.
    /// </summary>
    public interface ILobbyServiceSDKConfiguration
    {
        /// <summary>
        /// Sets the base path in configuration.
        /// </summary>
        /// <param name="basePath">The base path to set in configuration.</param>
        void SetBasePath(string basePath);

        /// <summary>
        /// Configures the SDK's behavior for lobby events generated by the local player.
        /// If enabled, events generated by the local player's updates will trigger callbacks the same way other players' updates do.
        /// If disabled, events generated by the local player's updates will be handled silently.
        /// This is disabled by default if no value has been set by the user.
        /// </summary>
        /// <param name="enabled">Whether or not to enable lobby event callbacks for updates made by the local player.</param>
        void EnableLocalPlayerLobbyEvents(bool enabled);
    }
}
